"""Ejercicio 5: Crear un programa que pida al usuario ingresar 2 números por teclado y
 devuelva por pantalla la suma de ellos en un renglón,
 la resta en otro, el producto en otros y la división en otro."""
 
x = int(input("valor de X: ")) 
y = int(input("valor de Y: "))

print(f"x + y = {x + y}") 
print(f"x - y = {x - y}") 
print(f"x * y = {x * y}") 
print(f"x / y = {x // y}") 